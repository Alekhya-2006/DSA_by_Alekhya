// Variables - Comtainers to store data.
// This program demonstrates how to declare variables of different data types 
// and how to print their values using cout.

#include <iostream> 
using namespace std; 

int main() {
    // Declaring an integer variable and assigning value 10
    int a = 10;

    // Declaring a floating point variable and assigning value 5.5
    float b = 5.5;

    // Declaring a character variable and assigning character 'A'
    char c = 'A';

    // Declaring a boolean variable and assigning it true (1)
    bool flag = true;

    // Printing the value of the integer variable
    cout << "Integer a: " << a << endl;

    // Printing the value of the float variable
    cout << "Float b: " << b << endl;

    // Printing the value of the char variable
    cout << "Char c: " << c << endl;

    // Printing the value of the boolean variable
    // true will be printed as 1, false as 0
    cout << "Boolean flag: " << flag << endl;

    return 0; 
}
